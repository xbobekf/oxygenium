class BIP32Spec extends AlephiumSpec {
  def decodeBtcXPrv(input: String): (SecP256K1PrivateKey, ByteString) = {
    val bs         = Base58.decode(input).get
    val rawKey     = bs.drop(46).take(32)
    val privateKey = SecP256K1PrivateKey.unsafe(rawKey)
    val chainCode  = bs.drop(13).take(32)
    (privateKey, chainCode)
  }

  def decodeBtcXPub(input: String): (SecP256K1PublicKey, ByteString) = {
    val bs        = Base58.decode(input).get
    val rawKey    = bs.drop(45).take(33)
    val publicKey = SecP256K1PublicKey.unsafe(rawKey)
    val chainCode = bs.drop(13).take(32)
    (publicKey, chainCode)
  }

  def decodeChain(input: String): AVector[Int] = {
    AVector
      .unsafe(input.split("/"))
      .tail
      .map(raw => if (raw.last equals '\'') BIP32.harden(raw.init.toInt) else raw.toInt)
  }

