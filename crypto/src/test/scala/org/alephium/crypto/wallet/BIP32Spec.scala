  def check(seed: ByteString, cases: Seq[(String, String, String)]): Unit = {
    val btcMasterKey = BIP32.btcMasterKey(seed)

    cases.foreach { case (chain, xpub, xprv) =>
      val (privateKey, chainCode0) = decodeBtcXPrv(xprv)
      val (publicKey, chainCode1)  = decodeBtcXPub(xpub)
      val path                     = decodeChain(chain)
      val extendedPrivateKey       = btcMasterKey.derive(path).get

      chainCode0 is chainCode1

      extendedPrivateKey.chainCode is chainCode0
      extendedPrivateKey.privateKey is privateKey
      extendedPrivateKey.derivationPath is chain
      privateKey.publicKey is publicKey

      if (path.forall(!BIP32.isHardened(_))) {
        val extendedPublicKey = btcMasterKey.extendedPublicKey.derive(path).get
        extendedPublicKey.chainCode is chainCode0
        extendedPublicKey.publicKey is publicKey
        extendedPublicKey.path is extendedPrivateKey.path
      }
    }
  }

